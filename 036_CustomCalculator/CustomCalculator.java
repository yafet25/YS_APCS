public class CustomCalculator 
{
    public int adder(int x, int y)
    {
        return x+y;
    }
    
    public int multi(int a, int z)
    {
        return a*z;
    }
    
    public int div(int e, int f)
    {
        return e/f;
    }

    public int mini(int c, int d)
    {
        return c-d;
    }

    public int area(int c, int h)
    {
        return c*h;
    }

    public int areaT(int b,int h )
    {
        return b*h/2;
    }
    
    public int perT(int a,int b, int c )
    {
        return a + b + c;
    }
    
    public int perL(int b, int h)
    {
        return 2 * b + h * 2;
    }
    
   
    public static void main(String[] args)
    {

    }
}